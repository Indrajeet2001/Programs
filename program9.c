/////////////////////////////////////////////////////////////////////////////////////////////////

#include<stdio.h>
#include<stdbool.h>

////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name :    DivisibleByFive
//  Description :      To check whether input is divisible by 5 or not
//  Input :            Integer
//  Output :           Integer
//  Author :           Indrajeet Vijay Kedari
//  Date :             12-10-2022
